#!/usr/bin/env node

/**
 * generate-cncf-outreach-issues.mjs
 *
 * Generates parameterized GitHub issue bodies for filing in each CNCF project's
 * repository. Each issue links to the KubeStellar Console, the project's AI mission,
 * and an "Improve this AI Mission" call-to-action.
 *
 * Usage:
 *   node scripts/generate-cncf-outreach-issues.mjs [--project=NAME] [--dry-run] [--output=DIR]
 *
 * Options:
 *   --project=NAME   Generate only for a specific project
 *   --dry-run        Print issues without writing files
 *   --output=DIR     Output directory (default: outreach-issues/)
 */

import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const __dirname = dirname(fileURLToPath(import.meta.url))

// Load CNCF projects list
const { CNCF_PROJECTS } = await import('./cncf-projects.mjs')

// Console base URL (configurable)
const CONSOLE_URL = process.env.CONSOLE_URL || 'https://console.kubestellar.io'
const KB_REPO = 'kubestellar/console-kb'

// Parse CLI args
const args = process.argv.slice(2)
const dryRun = args.includes('--dry-run')
const projectFilter = args.find(a => a.startsWith('--project='))?.split('=')[1]
const outputDir = args.find(a => a.startsWith('--output='))?.split('=')[1] || 'outreach-issues'

function slugify(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '')
}

function titleCase(str) {
  return str.replace(/(?:^|[-_])(\w)/g, (_, c) => ' ' + c.toUpperCase()).trim()
}

function generateIssueTitle(project) {
  return `🤖 AI-Powered Install Mission for ${titleCase(project.name)} — Community Feedback Welcome`
}

function generateIssueBody(project) {
  const slug = slugify(project.name)
  const displayName = titleCase(project.name)
  const missionUrl = `${CONSOLE_URL}/missions/install-${slug}`
  const improveUrl = `https://github.com/${KB_REPO}/issues/new?title=${encodeURIComponent(`Improve AI Mission: ${displayName}`)}&labels=ai-mission,community-improvement&body=${encodeURIComponent(`**Project:** ${displayName}\n**Section:** (install/uninstall/upgrade/troubleshooting)\n\n## What needs improvement?\n\n_Please describe what should be changed._`)}`

  return `## 🚀 AI-Generated Install Mission for ${displayName}

The [KubeStellar Console](${CONSOLE_URL}) now includes an **AI-generated installation mission** for **${displayName}**. This mission provides:

- ✅ **Step-by-step install guide** with copy-pasteable commands
- 🗑️ **Uninstall steps** for clean removal
- ⬆️ **Upgrade/update guide** with backup and rollback
- 🔧 **Troubleshooting** for common issues
- 📊 **Quality scored** by automated gates (schema, security, actionable commands, verification)

### 🔗 View the Mission

**[Open ${displayName} Install Mission →](${missionUrl})**

The mission was generated by synthesizing:
1. Your project's README and documentation
2. Helm charts and values.yaml
3. Kubernetes manifests and examples
4. Latest release information
5. Container image metadata
6. Common configuration patterns

### 🛠️ Help Us Improve It

As the maintainers of ${displayName}, you know your project best. If anything in the mission is:

- ❌ **Wrong** — a command is incorrect or outdated
- ➕ **Missing** — an important step is not covered
- 💡 **Improvable** — there's a better way to do it
- 🔒 **Security concern** — something should be done differently

**[📝 Suggest an Improvement →](${improveUrl})**

Or click the **"Improve this AI Mission"** button directly in the console when viewing the mission.

### 🤖 How It Works

The mission is auto-generated using:
- **6 knowledge sources** crawled from your GitHub repository
- **LLM synthesis** (GitHub Models) to produce structured install guides
- **7-gate quality checks** (schema, security, actionable commands, verification, version pinning)
- **Kind cluster integration testing** — commands are actually executed on a test cluster

Missions are regenerated weekly to stay current with new releases.

### 📦 About KubeStellar Console

[KubeStellar Console](${CONSOLE_URL}) is an open-source Kubernetes management console that provides:
- AI-powered missions for installing and managing CNCF projects
- Cluster monitoring, workload management, and security posture
- Community-driven mission marketplace

**Repository:** [github.com/${KB_REPO}](https://github.com/${KB_REPO})

---

_This issue was auto-generated as part of CNCF-wide outreach. The ${displayName} install mission is maintained in [\`fixes/cncf-install/install-${slug}.json\`](https://github.com/${KB_REPO}/blob/master/fixes/cncf-install/install-${slug}.json)._

_Labels: \`ai-mission\`, \`community\`, \`installation\`_
`
}

function generateIssueLabels() {
  return ['ai-mission', 'community', 'installation']
}

// ── Main ──────────────────────────────────────────────────────────────

// Check which projects have missions
const fixesDir = join(__dirname, '..', 'fixes', 'cncf-install')
let projects = CNCF_PROJECTS.filter(p => p.name !== 'kubestellar')

if (projectFilter) {
  projects = projects.filter(p => p.name === projectFilter)
  if (projects.length === 0) {
    console.error(`Project '${projectFilter}' not found in CNCF projects list`)
    process.exit(1)
  }
}

// Only generate outreach for projects that have missions
const projectsWithMissions = projects.filter(p => {
  const slug = slugify(p.name)
  const missionPath = join(fixesDir, `install-${slug}.json`)
  return existsSync(missionPath)
})

console.log(`Generating outreach issues for ${projectsWithMissions.length}/${projects.length} projects with missions`)

if (!dryRun) {
  mkdirSync(outputDir, { recursive: true })
}

const summary = { total: 0, generated: 0, skipped: 0 }

for (const project of projectsWithMissions) {
  summary.total++
  const slug = slugify(project.name)
  const title = generateIssueTitle(project)
  const body = generateIssueBody(project)
  const labels = generateIssueLabels()

  if (dryRun) {
    console.log(`\n${'═'.repeat(60)}`)
    console.log(`Project: ${project.name} (${project.repo})`)
    console.log(`Title: ${title}`)
    console.log(`Labels: ${labels.join(', ')}`)
    console.log(`${'─'.repeat(60)}`)
    console.log(body.slice(0, 500) + '...')
    summary.generated++
  } else {
    const outPath = join(outputDir, `${slug}.md`)
    const metadata = [
      `<!-- OUTREACH ISSUE for ${project.name} -->`,
      `<!-- Repo: ${project.repo} -->`,
      `<!-- Title: ${title} -->`,
      `<!-- Labels: ${labels.join(', ')} -->`,
      `<!-- To file: gh issue create --repo ${project.repo} --title "${title}" --body-file ${outPath} -->`,
      '',
    ].join('\n')
    writeFileSync(outPath, metadata + body)
    console.log(`  ✅ ${slug}.md`)
    summary.generated++
  }
}

console.log(`\n📊 Summary: ${summary.generated}/${summary.total} outreach issues generated`)
if (!dryRun) {
  console.log(`📁 Output: ${outputDir}/`)
  console.log(`\nTo file an issue for a specific project:`)
  console.log(`  gh issue create --repo OWNER/REPO --title "TITLE" --body-file ${outputDir}/PROJECT.md`)
}
